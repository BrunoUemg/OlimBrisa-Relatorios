    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if(txtDescricao.getText().isEmpty() || txtDataServico.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos !! ");
        }else{
        
        servicoM e = new servicoM();
        servicoDAO dao = new servicoDAO();
        
        
          e.setDescricao(txtDescricao.getText());
           e.setDataServico(txtDataServico.getText());
            
              e.setIdempresa((int) tbCliente.getValueAt(tbCliente.getSelectedRow(), 0));
             dao.create(e);
             
             txtRazaoSocial.setText("");
        txtId.setText("");
        txtDescricao.setText("");
        txtDataServico.setText("");
        desativaCampos();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

