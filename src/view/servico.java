    public void desativaCampos(){
    txtId.setText("");
    txtId.setEnabled(false);
    txtRazaoSocial.setText("");
    txtRazaoSocial.setEnabled(false);
    txtDataServico.setText("");
    txtDataServico.setEnabled(false);
    txtDescricao.setText("");
    txtDescricao.setEnabled(false);
    btnSalvar.setEnabled(false);
    btnCancelar.setEnabled(false);
    btnAlterarServico.setEnabled(false);
    btnCancelarServico.setEnabled(false);
    txtDtServico.setText("");
    txtDtServico.setEnabled(false);
    txtDescricaoServico.setText("");
    txtDescricaoServico.setEnabled(false);
    }
     public void ativaCampos(){
   
    txtId.setEnabled(false);
    
    txtRazaoSocial.setEnabled(true);
   
    txtDataServico.setEnabled(true);
   
    txtDescricao.setEnabled(true);
    btnSalvar.setEnabled(true);
    btnCancelar.setEnabled(true);
    
    
    
    }
     
     public void ativaCamposServico(){
     
         txtDtServico.setEnabled(true);
       txtDescricaoServico.setEnabled(true);
       btnCancelarServico.setEnabled(true);
         btnAlterarServico.setEnabled(true);
     }

