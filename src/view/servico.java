      public void readJTableForServico(String name){
        DefaultTableModel modelo = (DefaultTableModel) tbServico.getModel();
        modelo.setNumRows(0);
        servicoDAO cdao = new servicoDAO();
        
        for (servicoM c: cdao.readForServico(name)){
            
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getDataServico(),
                c.getDescricao(),
               
              
            });
        }
    }

